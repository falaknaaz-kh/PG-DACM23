class Q27 {
    public static void main(String[] args) {
        /**
         * The number of bits used to represent a double value.
         */
        System.out.println("The number of bits used to represent a double value is " + Double.SIZE);  //64

        //The number of bytes used to represent a double value.
        System.out.println("The number of bytes used to represent a double value is " + Double.BYTES);  //8

        //The minimum value a double can store.
        System.out.println("The minimum value a double variable can store is " + Double.MIN_VALUE);  //4.9E-324

        //The maximum value a double can store.
        System.out.println("The maximum value a double variable can store is " + Double.MAX_VALUE);  //1.7976931348623157E308

    }

}