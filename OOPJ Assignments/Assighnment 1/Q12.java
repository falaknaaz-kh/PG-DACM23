class Q12 {
    public static void main(String[] args) {
        /**
         * The number of bits used to represent a char value.
         */
        System.out.println("The number of bits used to represent a char value is " + Short.SIZE);  //16

        //The number of bytes used to represent a char value.
        System.out.println("The number of bytes used to represent a char value is " + Short.BYTES);  //2

        //The minimum value a char can store.
        System.out.println("The minimum value a short variable can store is " + Short.MIN_VALUE);  //-32768

        //The maximum value a char can store.
        System.out.println("The maximum value a short variable can store is " + Short.MAX_VALUE);  //32767

    }

}